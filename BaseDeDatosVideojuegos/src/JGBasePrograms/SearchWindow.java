    private void ButtonDeleteLabelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ButtonDeleteLabelMouseClicked

        DefaultTableModel dtm = (DefaultTableModel) PKMNTableList.getModel();
        if (PKMNTableList.getSelectedRow() == -1){
            JOptionPane.showMessageDialog(null, "No ha seleccionado ningún Pokémon para borrar.");
        } else {
            JOptionPane.showMessageDialog(null, "Se ha borrado el registro de la lista del Pokémon seleccionado.");
            dtm.removeRow(PKMNTableList.getSelectedRow());
        }
        
    }//GEN-LAST:event_ButtonDeleteLabelMouseClicked

