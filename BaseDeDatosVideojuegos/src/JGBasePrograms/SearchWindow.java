    public String condLegendario(String opcionLegendario) {
        switch (opcionLegendario) {
            case "No":
                return " WHERE legendario=false";
            case "Sí":
                return " WHERE legendario=true";
            default:
                return " WHERE legendario IS NOT NULL";
        }
    }

    public String condTipo1(String opcionTipo1) {
        if (opcionTipo1.equals("-")) {
            return " AND Tipo1 IS NOT NULL";
        } else {
            return " AND Tipo1='" + opcionTipo1 + "'";
        }
    }

    public String condTipo2(String opcionTipo2) {
        if (opcionTipo2.equals("-")) {
            return "";
        } else {
            return " AND Tipo2='" + opcionTipo2 + "'";
        }
    }

    public String condGen(String opcionGen) {
        switch (opcionGen) {
            case "Gen I":
                return " AND Gen=1";
            case "Gen II":
                return " AND Gen=2";
            case "Gen III":
                return " AND Gen=3";
            case "Gen IV":
                return " AND Gen=4";
            case "Gen V":
                return " AND Gen=5";
            case "Gen VI":
                return " AND Gen=6";
            case "Gen VII":
                return " AND Gen=7";
            case "Gen VIII":
                return " AND Gen=8";
            case "Gen IX":
                return " AND Gen=9";
            default:
                return " AND Gen IS NOT NULL";
        }
    }

